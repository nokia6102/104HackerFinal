    func getSql(){
        //執行sql指令
        let sql = "select JSI.job, C.name, JD.description,JD.others,JD.invoice from job_structured_info JSI,companies C,job_description JD, district D where D.district_no = JSI.addr_no and JD.jobno = JSI.jobno and JSI.invoice = C.invoice and JSI.salary_low > '\(searchSalarySql)' and (JSI.jobcat1 = '\(searchJobNo)' or JSI.jobcat2 = '\(searchJobNo)' or JSI.jobcat3 = '\(searchJobNo)') and D.district_name like '\(searchLocation)'"
