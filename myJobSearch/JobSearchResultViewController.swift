         */
        //print(sql!)
        
        //第二層  Where substr(yvtc_num,5,3) <> '000' and substr(yvtc_num,8,3) = '000'
        //第三層  Where substr(yvtc_num,5,3) <> '000' and substr(yvtc_num,8,3) <> '000'
        let cSql = sql.cString(using: .utf8)
        
        var statement:OpaquePointer? = nil
        
        sqlite3_prepare(db, cSql!, -1, &statement, nil)
        
        while sqlite3_step(statement) == SQLITE_ROW{
            let cJobName :String
            let cJobDesc : String
            let cJobOther: String
            let cJobInvoice: String
            
            let job_title = sqlite3_column_text(statement, 0)
            let cJobTitle = String(cString: job_title!)
            if let job_name = sqlite3_column_text(statement, 1){
                cJobName = String(cString: job_name)
            }else{
                cJobName = ""
            }
            //怕有空值，使用if let
            if let job_desc = sqlite3_column_text(statement, 2){
                cJobDesc = String(cString: job_desc)
            }else{
                cJobDesc = ""
            }
            if let job_other = sqlite3_column_text(statement, 3){
                cJobOther = String(cString: job_other)
            }else{
                cJobOther = ""
            }
            if let job_invoice = sqlite3_column_text(statement, 4){
                cJobInvoice = String(cString: job_invoice)
            }else{
                cJobInvoice = ""
            }
            //print(cJobTitle)
            dicRow = ["job_title": cJobTitle,"job_name": cJobName,"job_desc": cJobDesc,"job_other":cJobOther,"job_invoice":cJobInvoice]
            print(dicRow)
            resultJob.append(dicRow)
            //print(resultJob)
        }
        sqlite3_finalize(statement)
    }

